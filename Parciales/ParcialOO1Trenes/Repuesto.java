package ar.edu.unlp.info.oo1.ParcialOO1Trenes;

public class Repuesto {
	private String nombre;
	private double costo;
	
	public Repuesto(String nombre, double costo) {
		super();
		this.nombre = nombre;
		this.costo = costo;
	}

	public String getNombre() {
		return nombre;
	}

	public double getCosto() {
		return costo;
	}
	
	
}
